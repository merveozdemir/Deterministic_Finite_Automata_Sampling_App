    public State1 delta1(State1 q, char c) {
        if (q == State1.a && c == '1') {
            return State1.b;
        }
        if (q == State1.a && c == '0') {
            return State1.c;
        }
        if (q == State1.b && c == '0') {
            return State1.d;
        }
        if (q == State1.b && c == '1') {
            return State1.a;
        }
        if (q == State1.c && c == '0') {
            return State1.a;
        }
        if (q == State1.c && c == '1') {
            return State1.d;
        }
        if (q == State1.d && c == '1') {
            return State1.c;
        }
        if (q == State1.d && c == '0') {
            return State1.b;
        }
        return null;  //default is null -- no transition
    }

