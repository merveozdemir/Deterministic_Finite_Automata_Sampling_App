    boolean getResult(int deltaNo) {
        boolean result = false;

        switch (deltaNo) {
            case 1:
                Automaton dfa1 = new DFA1();
                result = dfa1.test(inputText.getText());
                break;
            case 2:
                Automaton dfa2 = new DFA2();
                result = dfa2.test(inputText.getText());
                break;
            case 3:
                Automaton dfa3 = new DFA3();
                result = dfa3.test(inputText.getText());
                break;
            case 4:
                Automaton dfa4 = new DFA4();
                result = dfa4.test(inputText.getText());
                break;
            default:
                break;
        }

        return result;
    }
    private void nextButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButonActionPerformed
        if (!finish) {
            char c = inputText.getText().charAt(nextCounter - 1);
            State1 t = null;
            if (deltaNo == 1) {
                t = delta1(s, c);
            } else if (deltaNo == 2) {
                t = delta2(s, c);
            } else if (deltaNo == 3) {
                t = delta3(s, c);
            } else if (deltaNo == 4) {
                t = delta4(s, c);
            }

            if (nextCounter == inputText.getText().length()) {
                turnOffTheButtons();
                if (inputText.getText().length() == 1) {
                    panelMap.get(nextCounter + 1).setVisible(true);
                    if (t == null) {
                        labelMap.get(nextCounter + 1).setText("(Ø, ε)");
                    } else {
                        labelMap.get(nextCounter + 1).setText("(" + t + ", ε)");
                        nextStat.setText("Next Statement : δ (" + t + ", ε)");
                    }
                    result = getResult(deltaNo);
                } else {
                    panelMap.get(nextCounter + 1).setVisible(true);
                    if (t == null) {
                        labelMap.get(nextCounter + 1).setText("(Ø, ε)");
                    } else {
                        labelMap.get(nextCounter + 1).setText("(" + t + ", ε)");
                        nextStat.setText("Next Statement : δ (" + t + ", ε)");
                    }
                    result = getResult(deltaNo);

                }
                
                if (result) {
                    AcceptRejectLbl.setText("'" + t + "' is final state,   ACCEPT!");
                } else {
                    if (t == null) {
                        AcceptRejectLbl.setText("it is not a state,    REJECT!");
                    } else {
                        AcceptRejectLbl.setText("'" + t + "' is not final state,    REJECT!");

                    }
                }
                 finish = true;

            } else if (nextCounter != inputText.getText().length()) {
                if (t != null) {
                    s = t;
                    //System.out.println(nextCounter + " - " + inputText.getText().length());
                    lightTheButton(s, inputText.getText().charAt(nextCounter));
                    nextStat.setText("Next Statement : δ (" + s + ", " + inputText.getText().charAt(nextCounter) + ") ");

                    panelMap.get(nextCounter + 1).setVisible(true);
                    labelMap.get(nextCounter + 1).setText("(" + s + ", " + inputText.getText().substring(nextCounter) + ")");
                    symbolMap.get(nextCounter + 1).setVisible(true);
                } else {
                    panelMap.get(nextCounter + 1).setVisible(true);
                    labelMap.get(nextCounter + 1).setText("( Ø , " + inputText.getText().substring(nextCounter) + ")");
                    symbolMap.get(nextCounter + 1).setVisible(true);

                    AcceptRejectLbl.setText("it is not a state,    REJECT!");
                    finish = true;

                }
            }
            nextCounter++;

        }
    }//GEN-LAST:event_nextButonActionPerformed

